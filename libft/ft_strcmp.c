int		ft_strcmp(char *s1, char *s2)
{
	unsigned char	a;
	unsigned char	b;
	unsigned int	i;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i])
	{
		i++;
	}
	a = s1[i];
	b = s2[i];
	return (a - b);
}
